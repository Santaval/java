package clases;

public class Persona {
    String nombre;
    double peso;
    int edad;
    int estatura;
    String apellidos;

    public Persona(String nombre, double peso, int edad, int estatura, String apellidos){
        this.nombre = nombre;
        this.peso = peso;
        this.edad = edad;
        this.estatura = estatura;
        this.apellidos = apellidos;
    }
}